package codigos_sessao_4;

// Importação da biblioteca Locale.
import java.util.Locale;

public class Aula04_AplicandoCasting {

	public static void main(String[] args) {
		
		// Definição das variáveis, sem atribuição de valores.
		int a, b;
		double resultado;
		
		// Definição posterior de valores nas variáveis.
		a = 5;
		b = 2;
		
		// Aplicação do Casting (Double).
		resultado = (double) a / b;
		
		// Exibição do resultado.
		System.out.println(resultado);
	}	
	
}
