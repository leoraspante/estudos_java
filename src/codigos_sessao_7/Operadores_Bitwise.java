// Exemplo prático de utilização dos operadores bitwise.

package codigos_sessao_7;

public class Operadores_Bitwise {

	public static void main(String[] args) {
		
		 // Declaração de variáveis com valores de exemplo.
		 int n1 = 89;
		 int n2 = 60;
		 
		 // Mensagem de saída.
		 System.out.println(n1 & n2); // Operador E.
		 System.out.println(n1 | n2); // Operador OU.
		 System.out.println(n1 ^ n2); // Operador OU-Exclusivo.
	}

}
